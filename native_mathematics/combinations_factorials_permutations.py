import math

# Example of using factorial
n = 5
factorial_of_n = math.factorial(n)
print(f"Factorial of {n}: {factorial_of_n}")

# Example of using permutations
n = 5
k = 3
permutations = math.perm(n, k)
print(f"Permutations of {n} elements taken {k} at a time: {permutations}")

# Example of using combinations
n = 5
k = 3
combinations = math.comb(n, k)
print(f"Combinations of {n} elements taken {k} at a time: {combinations}")
