my_tuple = ('zero', 1, 'two', 3, 'four')

print('--------all tuple------------')
print(my_tuple)
print('-----------------------------')

print('-------first element--------')
print(my_tuple[0])
print('-----------------------------')


print('-------for tuple------------')
for i in my_tuple:
    print(i)
print('-----------------------------')

print('--------three to final---------')
print(my_tuple[3:])
print('-----------------------------')

print('--------first to second-------')
print(my_tuple[:2])
print('-----------------------------')

print('------one to four-------')
print(my_tuple[1:4])
print('-----------------------------')

print('--------last element-------')
print(my_tuple[-1])
print('-----------------------------')
