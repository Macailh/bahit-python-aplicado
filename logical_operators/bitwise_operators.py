print('----Bitwise Operators----')

# Bitwise AND operator (&)
print(5 & 3)  # In binary: 101 & 011 = 001 (in decimal: 1)

# Bitwise OR operator (|)
print(5 | 3)  # In binary: 101 | 011 = 111 (in decimal: 7)

# Bitwise XOR operator (^)
print(5 ^ 3)  # In binary: 101 ^ 011 = 110 (in decimal: 6)

# Left shift (<<)
print(5 << 1)  # In binary: 101 shifted 1 left = 1010 (in decimal: 10)

# Right shift (>>)
print(5 >> 1)  # In binary: 101 shifted 1 right = 10 (in decimal: 2)

print('-------------------------')
