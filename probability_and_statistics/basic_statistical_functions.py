# Count elements      len(collection)
# Add elements        sum(collection)
# Get larger number   max(collection)
# Get smaller number  min(collection)


# Count elements
collection = [1, 2, 3, 4, 5]
count = len(collection)
print(f"Number of elements in the collection: {count}")

# Add elements
numbers = [10, 20, 30, 40, 50]
total = sum(numbers)
print(f"Sum of elements in the numbers list: {total}")

# Get larger number
values = [15, 25, 10, 35, 20]
largest = max(values)
print(f"Largest number in the values list: {largest}")

# Get smaller number
data = [8, 16, 4, 20, 12]
smallest = min(data)
print(f"Smallest number in the data list: {smallest}")
